    public void obtenerDatos(){
    List<Revista> revistas=new DAORevista().ObtenerDatos();
    DefaultTableModel modelo= new DefaultTableModel();
    String[] columnas={"numero","titulo","Ayo",
        "issn", "precio", "horaventa"};
    modelo.setColumnIdentifiers(columnas);
    for(Revista au:revistas){
        String[]renglon={Integer.toString(au.getNumero()),au.getTitulo(),
            Integer.toString(au.getAyo()),au.getIssn(), Float.toString(au.getPrecio()),
            au.getHoraventa().toString()};
            modelo.addRow(renglon);
    }
    jTableRevista.setModel(modelo);
}
