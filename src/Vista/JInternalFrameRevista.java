    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        // TODO add your handling code here:
        String tiT=jTextTitulo.getText();
        int ayO=Integer.parseInt(jTextAyo.getText());
        String issN=jTextIssn.getText();
        float preC=Float.parseFloat(jTextPrecio.getText());
        String horA=jTextHoraVenta.getText();
        if(tiT.contentEquals("")||ayO==0||
            issN.contentEquals("")||preC==0.0||
            horA.contentEquals("")){
            JOptionPane.showMessageDialog(rootPane,
                "Todos los campos son obligatorios");
        }else{
            try{
                Time hora=Time.valueOf(horA);
                Revista au=new DAORevista().Insertar(tiT, ayO, issN, preC, hora);
                JOptionPane.showMessageDialog(rootPane, "Registro agregado");

            }catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
            }
        }
        obtenerDatos();
        limpiarCampos();
    }//GEN-LAST:event_jBAgregarActionPerformed

    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        // TODO add your handling code here:
        int fila=this.jTableRevista.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            int numero=Integer.parseInt((String)this.jTableRevista.getValueAt(fila, 0).toString());
            DAORevista dao=new DAORevista();
            dao.Eliminar(numero);
            obtenerDatos();
        }
    }//GEN-LAST:event_jBBorrarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        int fila=this.jTableRevista.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            try{
                int numero=Integer.parseInt((String)this.jTableRevista.getValueAt(fila, 0).toString());
                String tit=(String)this.jTableRevista.getValueAt(fila,1);
                int ayo=Integer.parseInt((String)this.jTableRevista.getValueAt(fila, 2).toString());
                String issn=(String)this.jTableRevista.getValueAt(fila,3);
                float prec=Float.parseFloat((String)this.jTableRevista.getValueAt(fila, 4).toString());
                Time hora=Time.valueOf((String)this.jTableRevista.getValueAt(fila,5).toString());

                jTextNumero.setText(""+numero);
                jTextTitulo.setText(tit);
                jTextAyo.setText(String.valueOf(ayo));
                jTextIssn.setText(issn);
                jTextPrecio.setText(String.valueOf(prec));
                jTextHoraVenta.setText(String.valueOf(hora));
            }catch(NumberFormatException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed

    public void actualizarRevista(){
        int numero=Integer.parseInt(this.jTextNumero.getText());
        String tit=this.jTextTitulo.getText();
        int ayo=Integer.parseInt(this.jTextAyo.getText());
        String issn=this.jTextIssn.getText();
        float prec=Float.parseFloat(this.jTextPrecio.getText());
        Time hora=Time.valueOf(this.jTextHoraVenta.getText());
        
        DAORevista dao=new DAORevista();
        int res=dao.Actualizar(numero, tit, ayo, issn, prec, hora);
        if(res==1){
            JOptionPane.showMessageDialog(rootPane, "¡Revista Actualizada!");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
        }
    }
    
    
